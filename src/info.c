// info.c - Information menu actions for OricScreenEditorLOCI (Oric Atmos,
// Oscar64)
//
// info_version_show() is now an IDENTICAL layout to locifilemanager-v2's
// versioninfo()
// (https://github.com/xahmol/locifilemanager-v2/blob/main/src/main.c +
// src/splash_data.h) -- same 2-page sequence (logo + version/credits text
// sharing page 1, QR code alone on page 2), same idi8b_logo[] artwork
// (copied verbatim below, see its own credit comment), same QR-generation
// approach (tools/gen_qr.js, this project's TEXT changed to OSE's own
// GitHub URL). Adapted: window save/restore uses this project's main-RAM
// menu_winsave()/menu_winrestore() instead of locifilemanager-v2's
// overlay-RAM menu_popup_open/close(). An earlier draft of this file used
// OSE's own title-screen image (assets/OSETSC.BIN) as a dedicated first
// page instead -- reverted per explicit instruction to match
// locifilemanager-v2 exactly; that asset is used for the boot splash
// instead (src/main.c).
//
// info_exit() has no V1/locifilemanager-v2 precedent: V1 (CC65) just
// returns control to the OS-level tape loader, but OSE's bare-metal Oscar64
// runtime replaced that loader entirely at boot (see CLAUDE.md "Memory
// Layout"), so there is nothing to "return" to. The safe, hardware-
// documented equivalent (~/.claude/oric_atmos_reference.md's vector table)
// is a software reset through the RESET vector ($FFFC/$FFFD) -- NOT a
// direct call to the cold-start handler at $F88F, which the reference
// explicitly warns must only be invoked via the CPU RESET pin.

